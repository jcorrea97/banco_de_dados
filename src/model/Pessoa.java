package model;

public class Pessoa {
	
	private Integer id_pessoa;
	private String nome_pessoa;
	private Integer id_pessoa_emprestimo;
	public Integer getId_pessoa() {
		return id_pessoa;
	}
	public void setId_pessoa(Integer id_pessoa) {
		this.id_pessoa = id_pessoa;
	}
	public String getNome_pessoa() {
		return nome_pessoa;
	}
	public void setNome_pessoa(String nome_pessoa) {
		this.nome_pessoa = nome_pessoa;
	}
	public Integer getId_pessoa_emprestimo() {
		return id_pessoa_emprestimo;
	}
	public void setId_pessoa_emprestimo(Integer id_pessoa_emprestimo) {
		this.id_pessoa_emprestimo = id_pessoa_emprestimo;
	}
	

}
