package model;

public class Artigo_de_livros {
	
	private Integer id_pub;
	private String titulo_artigo;
	private Integer num_edicao;
	private Integer num_cap;
	private Integer id_artigo;
	private Integer ano_pub;
	
	public Integer getId_pub() {
		return id_pub;
	}
	public void setId_pub(Integer id_pub) {
		this.id_pub = id_pub;
	}
	public String getTitulo_artigo() {
		return titulo_artigo;
	}
	public void setTitulo_artigo(String titulo_artigo) {
		this.titulo_artigo = titulo_artigo;
	}
	public Integer getNum_edicao() {
		return num_edicao;
	}
	public void setNum_edicao(Integer num_edicao) {
		this.num_edicao = num_edicao;
	}
	public Integer getNum_cap() {
		return num_cap;
	}
	public void setNum_cap(Integer num_cap) {
		this.num_cap = num_cap;
	}
	public Integer getId_artigo() {
		return id_artigo;
	}
	public void setId_artigo(Integer id_artigo) {
		this.id_artigo = id_artigo;
	}
	public Integer getAno_pub() {
		return ano_pub;
	}
	public void setAno_pub(Integer ano_pub) {
		this.ano_pub = ano_pub;
	}

}
